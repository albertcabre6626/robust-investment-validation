def simulate_gbm(S0=100, mu=0.0, sigma=0.2, T=1.0, N=252, seed=None):
    if seed is not None:
        np.random.seed(seed)

    dt = T / N
    t = np.linspace(0, T, N + 1)

    S = np.zeros(N + 1)
    S[0] = S0

    for i in range(1, N + 1):
        Z = np.random.normal(0, 1)
        S[i] = S[i-1] * np.exp((mu - 0.5 * sigma**2) * dt + sigma * np.sqrt(dt) * Z)

    return t, S
