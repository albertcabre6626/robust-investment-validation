def generate_scenarios(n_per_type=10, sigma=0.2, T=1.0, N=252):
    scenarios = []

    types = [
        {"type": "no_signal", "mu": 0.0},
        {"type": "positive_drift", "mu": 0.10},
        {"type": "negative_drift", "mu": -0.10},
    ]

    seed = 1200

    for tinfo in types:
        for _ in range(n_per_type):
            _, S = simulate_gbm(
                S0=100,
                mu=tinfo["mu"],
                sigma=sigma,
                T=T,
                N=N,
                seed=seed
            )

            scenarios.append({
                "id": seed,
                "type": tinfo["type"],
                "mu": tinfo["mu"],
                "sigma": sigma,
                "prices": [round(x, 4) for x in S.tolist()]
            })
            seed += 1

    return scenarios
