    plot_df.plot(kind="bar", stacked=True)
    plt.title("Distribución de posiciones por estrategia")
    plt.xlabel("Estrategia")
    plt.ylabel("Proporción")
    plt.xticks(rotation=20)
    plt.tight_layout()
    plt.savefig(output_path, dpi=160)
    plt.close()


